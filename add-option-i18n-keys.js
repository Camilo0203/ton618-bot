function translateText(text) {
  let translated = text;
  
  // Aplicar traducciones palabra por palabra
  Object.keys(translations).forEach(en => {
    const es = translations[en];
    const regex = new RegExp(`\\b${en}\\b`, 'g');
    translated = translated.replace(regex, es);
  });
  
  return translated;
}

// Generar estructura para cada comando
const enStructure = {};
const esStructure = {};

Object.keys(data.byCommand).sort().forEach(cmdName => {
  enStructure[cmdName] = { options: {} };
  esStructure[cmdName] = { options: {} };
  
  data.byCommand[cmdName].forEach(opt => {
    const simpleKey = `${opt.path.replace(/\./g, '_')}_${opt.optionName}`;
    enStructure[cmdName].options[simpleKey] = opt.description;
    esStructure[cmdName].options[simpleKey] = translateText(opt.description);
  });
});

// Guardar estructuras
fs.writeFileSync('option-keys-en.json', JSON.stringify(enStructure, null, 2));
fs.writeFileSync('option-keys-es.json', JSON.stringify(esStructure, null, 2));

