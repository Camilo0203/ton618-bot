// Helper function to translate based on context
function translateToSpanish(key, enValue) {
  if (!enValue || typeof enValue !== 'string') return enValue;
  
  // Try exact match first
  if (translations[enValue.toLowerCase()]) {
    return translations[enValue.toLowerCase()];
  }
  
  // Try to translate common patterns
  let translated = enValue;
  
  // Replace common words
  Object.entries(translations).forEach(([en, es]) => {
    const regex = new RegExp('\\b' + en + '\\b', 'gi');
    translated = translated.replace(regex, es);
  });
  
  return translated;
}

